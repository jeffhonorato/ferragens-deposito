const pisoCard1 = () => {
    const pisoCores = document.querySelectorAll(".js-img-cor-piso-1");
    const pisoGrande = document.querySelector(".js-piso-img-card-1");

    pisoCores.forEach((cor) => {
        cor.addEventListener("click", (e) => {
            pisoGrande.src = cor.src;
            e.preventDefault();
        });
    });
};

